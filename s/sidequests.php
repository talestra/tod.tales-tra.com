    <h2>¡Bodegas con tesoros!</h2>
    <p>Todos los puertos de todas las ciudades tienen al menos una bodega. Aunque parezcan vacías e inútiles, puedes encontrar objetos realmente buenos moviendo las cajas que hay dentro y poniéndolas en la posición correcta.</p>
    <p>Teniendo en cuenta que describir la manera de ordenar las cajas puede resultar complicado, hemos incluido unas imágenes en las que se puede apreciar en qué posición hay que colocarlas, de acuerdo al color de los recuadros.</p>
    <p>Formación naranja, azul o verde significa que para obtener el objeto oculto tienes que colocar todas las cajas en los cuadrados del color correspondiente.</p>
    <p>Por otra parte, en el juego existe un objeto llamado Farol, localizado en la mazmorra opcional y que permite ver los puntos donde se deben colocar las cajas. No obstante, estos objetos te serán más útiles cuando llegues por primera vez a la ciudad de turno.</p>
