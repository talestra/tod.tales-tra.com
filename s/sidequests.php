    <h2>Un ascenso al infierno – La Torre de Druaga</h2>
    <h3>Explicación y estructura</h3>
    <p>La Torre de Druaga está basada en un viejo juego de arcade de Namco, del mismo nombre. En esta torre hay un tesoro en cada una de las 60 plantas, que aparece tras hacer una serie específica de complicadas acciones. La recompensa por esta larga y tediosa mazmorra es una enorme cantidad de experiencia y los mejores equipamientos del juego.</p>
    <p>La torre se estructura en 60 pisos con una música pegadiza que acabarás odiando. Cada piso está formado por columnas y paredes horizontales y verticales, formando una suerte de laberinto. Las paredes, no obstante, se pueden romper, haciendo que avances rápido. Cada piso tiene una llave, un cofre y una puerta. Sin la llave no puedes abrir la puerta, y una vez abierta ésta se transformará en unas escaleras, por lo cual «abrir la puerta» y «subir un piso» no es lo mismo.</p>
    <p>Los cofres son algo más complicados. Tienen tesoros, muy buenos tesoros, pero sólo aparecen al realizar una serie de acciones, algunas terriblemente complicadas e imposibles de imaginar por uno mismo. Algunos tesoros son además necesarios para completar la torre con éxito.</p>
    <p>En la torre no hay encuentros aleatorios, verás a todos los enemigos pululando por el piso, y con tocarlos combatirás contra ellos. Es importante no matar indiscriminadamente y luego mirar en esta guía cómo se obtiene el tesoro, porque ambas cosas suelen estar relacionadas.</p>
    <p>Recuerda, sólo se puede salir de la torre antes de terminarla usando unas Botas aladas.</p>
    <h3>Requisitos</h3>
    <p>Antes de entrar a la torre es aconsejable que hayas subido tus estadísticas con las hierbas de la tienda de lentes, especialmente el TP de todos los personajes en general y el de Philia en particular. También compra muchos objetos curativos, aunque es probable que siguiendo esta guía no los necesites.</p>
    <p>Para entrar en la torre necesitas las tres varas de la leyenda de Ishtar: la Vara CV, la Vara CR, y la Vara CA. La CV estaba en Cloudius, así que si no la cogiste, deja de leer porque no podrás entrar en la mazmorra: GAME OVER. Otra de las varas está en Harmentz y la tiene Walter, aunque tendrás que usar un Pomo rúnico o Identificar para que se transforme en la vara que necesitas, así que ve a cogerla si no la tienes.</p>
    <p>La torre en sí está en Phandaria, donde te encuentras con Rutee casi al principio del juego. En esa misma sala debes poner las tres varas y se abrirá la entrada a la torre.</p>
    <h3>La estrategia</h3>
    <p>¿Cómo superar este monstruo del averno y vivir para contarlo? ¿Un objeto secreto? ¿Extrañas combinaciones de botones? ¿Una técnica especial? De todas las preguntas, la respuesta correcta es la última: una simple, muy simple técnica, te dará la victoria sobre esta torre y los monstruos que en ella moran: el «Paro» de Philia.</p>
    <p>Aunque Paro suena a típico hechizo de RPG demasiado bueno para ser real, lo cierto es que funciona siempre contra cualquier enemigo. Al luchar vas al menú, haces Paro, Philia baila y el tiempo se para lo suficiente para que puedas asesinar a todos los enemigos con la mayor brutalidad posible sin que te toquen ni un pelo. En resumen: UTILÍZALO.</p>
    <p>Es una técnica barata en cuanto a TP, por eso si Philia tiene el suficiente, recuperara lo perdido en Paro al final de cada batalla, proporcionándote la ilimitada capacidad de aniquilar a todo desgraciado enemigo que se te acerque.</p>
    <p>Por desgracia, la técnica no funciona con el Quox, Druaga y otros jefes de la torre, sólo con los enemigos normales, que son la inmensa mayoría.</p>
