    <h2>Técnicas sagradas y místicas</h2>
    <p>Las técnicas sagradas son técnicas especiales que sólo puede ejecutar Stahn llevando equipada su Swordian Dymlos. Estas técnicas, además de ser enlazables con ciertas técnicas normales para hacer combos de gran potencia, suelen estar basadas en una técnica normal de Stahn y un hechizo de Dymlos, dando como resultado una técnica que sólo pueden usar los dos juntos.</p>
    <p>A lo largo del juego hay distintos monolitos con un texto en principio indescifrable, pero puedes leerlos con las técnicas, hechizos y niveles de Stahn y Dymlos adecuados. Cuando esas cuatro condiciones se cumplen, te realizarán dos preguntas, cuyas respuestas son una técnica de Stahn y un hechizo de Dymlos. Es bastante normal encontrar distintos monolitos a lo largo del juego que no puedes leer a pesar de tener la técnica y el hechizo requerido, y es que los niveles requeridos suelen ser bastante altos.</p>
    <p>Paradójicamente, los primeros monolitos que te encontrarás serán los de las últimas técnicas sagradas que podrás aprender.</p>
    <p>Hay un total de ocho técnicas sagradas, y la última sólo podrás conseguirla en la recta final del juego. Además hay dos técnicas que están en un lugar que a partir de cierto punto del juego no podrás volver a visitar.</p>
    <p>La lista a continuación describe las técnicas sagradas así como su lugar y momento de aprendizaje.</p>
    <h3>VUELO DEL FÉNIX</h3>
    <ul>
      <li><strong>Lugar</strong>: cueva al sur de Sheeden.</li>
      <li><strong>Momento</strong>: cuando te encuentres con el dragón marino Bernardo es muy posible que ya puedas aprenderla.</li>
      <li><strong>Nivel requerido de Stahn</strong>: 20</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 22</li>
      <li><strong>Requisitos</strong>: hechizo «Bola de fuego» y técnica «Tempestad».</li>
    </ul>
    <h3>LANZAR ESPADA</h3>
    <ul>
      <li><strong>Lugar</strong>: pueblo de Armeida, Seingald.</li>
      <li><strong>Momento</strong>: Cless y Arche sólo aparecen en la segunda mitad del juego, así que podrás conseguirla en cuanto puedas acceder a Armeida.</li>
      <li><strong>Nivel requerido de Stahn</strong>: -</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 8</li>
      <li><strong>Requisitos</strong>: tendrás que acertar, como mínimo, 45 de las 50 preguntas del test de Cless y Arche. Están en un lateral de la casa que está subiendo una colina. Las respuestas y más detalles, en su sección correspondiente.</li>
    </ul>
    <h3>ONDA LLAMEANTE</h3>
    <ul>
      <li><strong>Lugar</strong>: pasaje subterráneo de Heidelberg.</li>
      <li><strong>Momento</strong>: posiblemente sea la primera técnica sagrada que puedas aprender. Si has ido entrenando adecuadamente es muy posible que la obtengas en ese momento. Y de hecho no tendrás otra oportunidad, porque nunca más podrás volver a entrar en el pasaje subterráneo una vez salgas.</li>
      <li><strong>Nivel requerido de Stahn</strong>: 25</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 23</li>
      <li><strong>Requisitos</strong>: hechizo «Muro ígneo» y la técnica «Golpe draco».</li>
    </ul>
    <h3>VUELO DEL DRAGÓN</h3>
    <ul>
      <li><strong>Lugar</strong>: pueblo de Lienea, Fitzgald</li>
      <li><strong>Momento</strong>: en la segunda mitad del juego, posiblemente tras superar varias o todas las Aerópolis, que seguramente habrán proporcionado experiencia suficiente.</li>
      <li><strong>Nivel requerido de Stahn</strong>: 33</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 34</li>
      <li><strong>Requisitos</strong>: hechizo «Tormenta de fuego» y técnica «Danza del ángel».</li>
    </ul>
    <h3>TORNADO ÍGNEO</h3>
    <ul>
      <li><strong>Lugar</strong>: laboratorio Swordian I+D al norte de Armeida.</li>
      <li><strong>Momento</strong>: deberías llegar al laboratorio con el nivel suficiente para aprender esta técnica. De no ser así, tendrás que entrenar.</li>
      <li><strong>Nivel requerido de Stahn</strong>: 40</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 42</li>
      <li><strong>Requisitos</strong>: hechizo «Explosión» y técnica «Tornado».</li>
    </ul>
    <h3>BESTIA INFERNAL</h3>
    <ul>
      <li><strong>Lugar</strong>: Aerópolis Cloudius</li>
      <li><strong>Momento</strong>: tendrás que tener el nivel requerido para aprenderla en cuanto la encuentres porque una vez pasada la segunda mitad del juego, no podrás volver a la Aerópolis.</li>
      <li><strong>Nivel requerido de Stahn</strong>: 38</li>
      <li><strong>Nivel requerido de Dymlos</strong>: 38</li>
