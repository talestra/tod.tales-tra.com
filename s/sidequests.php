    <h2>El corredor de Neuestadt</h2>
    <p>En la parte superior derecha de Neuestadt, en el parque, puedes competir con el chico más rápido de la ciudad por una serie de premios. Los organizadores te enseñarán el circuito antes de correr. Realmente no hace falta memorizarlo, las flechas indican el camino.</p>
    <h3>Primer encuentro</h3>
    <p>Este encuentro sólo puedes hacerlo en la primera mitad del juego, cuando visites Neuestadt por primera vez.</p>
    <p>Si te mueves antes de que termine la cuenta atrás, ésta empezará de nuevo. Hay una manera de tomarle la delantera al chico nada más empezar: cuando la cuenta atrás comience, hablas con el chico, pero sin pasar el diálogo. Esto hará que el niño no se mueva inmediatamente después de que termine la cuenta atrás. Dejando pulsado el botón izquierdo y pulsando y manteniendo el botón X podrás pasar el diálogo y salir corriendo con ventaja.</p>
    <p>Con esta estrategia el resto de la carrera es muy fácil, en dos o tres intentos cualquiera debería ganar. Hay que intentar correr de forma eficiente: usando la parte interior del circuito para reducir el camino al girar, por ejemplo.</p>
    <p>Una vez victorioso podrás escoger uno de tres premios. Podrás correr dos veces más y elegir cada vez el mismo premio, no hay problema:</p>
    <ol>
      <li>Salvia [aumenta permanentemente el HP en un 5%]</li>
      <li>Carmín mágico [proporciona un 10% de descuento en tiendas]</li>
      <li>Manto de ladrón [Defensa +3, Evasión +5%]</li>
    </ol>
    <p>Cuando ganes tres veces el chico no querrá volver a correr, y recibirás el título de «Príncipe de la velocidad punta».</p>
    <h3>Segundo encuentro</h3>
    <p>En la segunda mitad del juego podrás volver a correr otra vez contra el chico rápido y conseguir nuevos premios. Esta carrera es bastante más difícil, porque ahora acelera en ciertos puntos que le hacen ir mucho más rápido.</p>
    <p>Una buena salida ya no es suficiente, para ganar hay que tratar de ponerse justo enfrente de él cual cuando vaya a bajar las primeras escaleras, que es cuando acelera por primera vez. Requiere varios intentos.</p>
    <p>Como antes, hay tres premios:</p>
    <ol>
      <li>Elixir [recupera completamente el HP y TP]</li>
      <li>Pomo encantado [proporciona un gran descuento en tiendas]</li>
      <li>Gnome [invoca a Gnome, espíritu protector de la tierra]</li>
    </ol>
    <p>Como antes, el chico ya no querrá volver a correr tras su tercera derrota y te otorgará otra vez el título de «Príncipe de la velocidad punta».</p>
