    <h2>El coliseo de Neuestadt</h2>
    <p>El torneo que tiene lugar en el Coliseo de Neuestadt comprende tres fases diferentes, cada una de las cuales contiene diferentes monstruos y premios. A continuación se incluye una lista de enemigos, premios, rondas, y algunos consejos.</p>
    <h3>Fase 1</h3>
    <p>Tras la batalla contra los navíos piratas en Neuestadt, y antes de partir hacia Aquaveil, puedes luchar en la primera fase del coliseo. Tienes que comprar los tíquets en la entrada fuera del coliseo.</p>
    <p>Al final de cada ronda, y a menos que se le reclute, tendrás que enfrentarte al campeón, Kongman. Esta pelea es muy difícil en este punto del juego, casi imposible, aunque tiene un truco: tienes que lanzarle constantemente Balas eléctricas. Tirándole quince, y esperando un poco a que se le pase el efecto de cada una, que si no se queda en el aire y falla, Kongman debería estar muerto o casi. Tras vencerle se unirá al grupo y no habrá que volver a preocuparse por él. Si tienes algún objeto de invocación que hayas conseguido en alguna bodega, también puedes usarlo, le hará un daño brutal.</p>
    <p>La lista de enemigos de esta fase antes del campeón es la siguiente:</p>
    <ol>
      <li>Bárbaro</li>
      <li>Hombre lagarto</li>
      <li>Escorpión</li>
      <li>Gusano</li>
      <li>Abejorro</li>
      <li>Culebra venenosa</li>
      <li>Tzin</li>
      <li>Mandrágora</li>
    </ol>
    <p>Los enemigos son fáciles en su mayoría, no deberían dar problemas. Tras la derrota de Kongman querrá unirse al grupo, aceptar es imperativo.</p>
    <p>Tras la derrota de Kongman o bien el último monstruo, recibirás lo siguiente:</p>
    <ul>
      <li>1ª Ronda - Elixir</li>
      <li>2ª Ronda – Juego de hierbas: Ajedrea, Salvia, Lavanda, Belladona</li>
      <li>3ª Ronda – Gorro de la suerte</li>
      <li>4ª Ronda – Juego de Gourmet: Mermelada, Sopa de cangrejo, Ternera con salsa, Arenque</li>
      <li>5ª Ronda – Juego de geles: Gel de piña, Gel de limón, Gel mixto, Gel de naranja</li>
    </ul>
    <p>Tras la quinta ronda, se seguirá con los juegos de geles.</p>
    <h3>Fase 2</h3>
    <p>En la segunda mitad del juego, con el resurgimiento de las Aerópolis, puedes acceder a la segunda fase del coliseo. Los monstruos son los siguientes:</p>
    <ol>
      <li>Diablillo</li>
      <li>Gólem de barro</li>
      <li>Súper Gigante</li>
      <li>Cieno sangriento</li>
      <li>Zombi</li>
      <li>Lobo sangriento</li>
      <li>Estrella maligna</li>
      <li>Gólem de hielo</li>
    </ol>
    <p>Monstruos fáciles y un Kongman aún más fácil. Tiene el mismo nivel que la última vez, mientras que tú habrás mejorado mucho. Los premios también son mejores.</p>
    <ul>
      <li>1ª Ronda – Fruta del gigante</li>
      <li>2ª Ronda – Espada ardiente</li>
      <li>3ª Ronda – Llama espiral</li>
      <li>4ª Ronda – Juego de hierbas de calidad: Ajedrea, Salvia, Lavanda, Belladona, Ajedrea roja, Salvia roja, Lavanda roja, Belladona roja</li>
      <li>5ª Ronda – Juego de geles potentes:  Gel de limón, Gel de piña, Gel milagroso, Elixir</li>
    </ul>
    <p>Tras la quinta ronda seguirás recibiendo juegos de geles potentes, lo que se traduce en elixires infinitos.</p>
    <h3>Fase 3</h3>
    <p>En la recta final del juego, tras la reparación completa de la Aethereosfera, podrás acceder a la última fase del coliseo. En esta ocasión el último enemigo puede dar problemas porque absorbe el fuego. Siendo así, o le acribillas a Golpes draco, o usas un arma que no sea Dymlos. Preferiblemente la Lágrima de Bahamut, Excalibur o Clemente. Los enemigos en esta ocasión son:</p>
    <ol>
      <li>Trent</li>
      <li>Basilisco</li>
      <li>Nube de gas</li>
      <li>Grizzly</li>
      <li>Búho asesino</li>
      <li>Perchero rojo</li>
      <li>Baldios</li>
      <li>Wyvern</li>
    </ol>
    <p>Los premios no son gran cosa, pero continúa la posibilidad de seguir consiguiendo elixires ilimitados. Completar todas las rondas da acceso a un minijuego.</p>
    <ul>
      <li>1ª Ronda – Comando Combo</li>
      <li>2ª Ronda - Canalizador: Canalizador</li>
      <li>3ª Ronda – Juego de joyas: Diamante, Labradorita, Rubí, Esmeralda, Sardónice</li>
      <li>4ª Ronda – Manto plateado</li>
      <li>5ª Ronda – Juego de geles potentes: Gel de limón, Gel de piña, Gel milagroso, Elixir</li>
    </ul>
