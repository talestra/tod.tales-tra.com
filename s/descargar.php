  <h1>Descargar</h1>
  <p>El proceso de parcheo es muy sencillo. Simplemente has de descargar el parcheador que se encuentra un poco más abajo, ejecutarlo, abrir una imagen del <em>Tales of Destiny</em> versión americana, en formato *.cue / *.bin (concretamente el archivo *.bin). Al darle a Traducir, el parcheador te preguntará dónde quieres guardar la nueva imagen del juego ya traducido. Esperas hasta que termine, y listo. Ya puedes jugarlo en tu PlayStation, en un emulador de dicha consola para PC, o en una PSP.</p>
  <div align="center"> <img src="img/parcheador.jpg" alt="" width="446" height="362" /> </div>
  <p>Por defecto se sustituirá el opening americano del juego por el japonés. Si prefirieras tener el americano, desactiva la casilla <em>Opening JAP</em>.</p>
  <p>Para más información, pásate por la sección <a href="faq.html">FAQ</a> (preguntas más frecuentes).</p>
